package tienda;

public class Tienda {
	private int idTienda;
	private String nombreTienda;
	private String calleTienda;
	/**
	 * @return the idTienda
	 */
	public int getIdTienda() {
		return idTienda;
	}
	/**
	 * @param idTienda the idTienda to set
	 */
	public void setIdTienda(int idTienda) {
		this.idTienda = idTienda;
	}
	/**
	 * @return the nombreTienda
	 */
	public String getNombreTienda() {
		return nombreTienda;
	}
	/**
	 * @param nombreTienda the nombreTienda to set
	 */
	public void setNombreTienda(String nombreTienda) {
		this.nombreTienda = nombreTienda;
	}
	/**
	 * @return the calleTienda
	 */
	public String getCalleTienda() {
		return calleTienda;
	}
	/**
	 * @param calleTienda the calleTienda to set
	 */
	public void setCalleTienda(String calleTienda) {
		this.calleTienda = calleTienda;
	}

}
